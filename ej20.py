# Juego de Ahorcado:

# Escribe un programa en Python que simule el juego de ahorcado. El programa debe realizar lo siguiente:

# 1. Tener una lista de palabras para adivinar.
# 2. Seleccionar aleatoriamente una palabra de la lista.
# 3. Mostrar al jugador la palabra como una serie de guiones bajos (uno por cada letra).
# 4. Permitir al jugador ingresar letras para adivinar la palabra.
# 5. Mostrar la palabra con las letras adivinadas y los guiones bajos restantes.
# 6. Limitar la cantidad de intentos que el jugador puede realizar antes de perder.
# 7. Indicar al jugador si ganó o perdió al final del juego.
